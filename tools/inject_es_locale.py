def _esc(s: str) -> str:
    """Escape for Dart single-quoted string. Preserves \\n, \\r, \\$ as Dart escapes (not \\\\)."""
    s = s.replace("\r", "\\r").replace("\n", "\\n")
    out: list[str] = []
    i = 0
    while i < len(s):
        c = s[i]
        if c == "'":
            out.append("\\'")
            i += 1
        elif c == "\\" and i + 1 < len(s):
            nxt = s[i + 1]
            if nxt == "$":
                out.append("\\$")
                i += 2
            elif nxt == "n":
                out.append("\\n")
                i += 2
            elif nxt == "r":
                out.append("\\r")
                i += 2
            else:
                out.append("\\\\")
                i += 1
        elif c == "\\":
            out.append("\\\\")
            i += 1
        else:
            out.append(c)
            i += 1
    return "".join(out)


def build_es_block() -> str:
    keys = list(ES_MAP.keys())
    if len(keys) != 332:
        raise SystemExit(f"ES_MAP debe tener 332 claves, tiene {len(keys)}")
    lines = ["    'es': {"]
    for k in keys:
        lines.append(f"      '{k}': '{_esc(ES_MAP[k])}',")
    lines.append("    },")
    return "\n".join(lines)


def main() -> None:
    text = DART.read_text(encoding="utf-8")
    new_es = build_es_block()
    pattern = r"    'es': \{.*?\n    \},\n    'ur':"
    if not re.search(pattern, text, flags=re.DOTALL):
        raise SystemExit("No se encontró el bloque es")
    # Callable repl: str repl would interpret \\n as newline and corrupt Dart \\n escapes.
    text2 = re.sub(
        pattern,
        lambda _m: new_es + "\n    'ur':",
        text,
        count=1,
        flags=re.DOTALL,
    )
    DART.write_text(text2, encoding="utf-8")
    print("OK: es locale inyectado (332 claves).")


if __name__ == "__main__":
    main()
