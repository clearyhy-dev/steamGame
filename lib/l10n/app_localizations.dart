  /// 后台/Workmanager 用：按语言码取文案，无 context 时使用
  static String getStringForLocale(String? localeCode, String key) {
    final code = (localeCode != null && localeCode.isNotEmpty && _localized.containsKey(localeCode))
        ? localeCode
        : 'en';
    final en = _localized['en'];
    return _localized[code]?[key] ?? en?[key] ?? key;
  }

  String _localeCode() {
    final code = locale.languageCode;
    if (_localized.containsKey(code)) return code;
    return 'en';
  }

  String get(String key) {
    final code = _localeCode();
    final en = _localized['en'];
    return _localized[code]?[key] ?? en?[key] ?? key;
  }

  /// Replaces `{price}` in keys like [trial_099], [monthly_299], [yearly_1699].
  String subscriptionPriceLabel(String key, String formattedPrice) {
    return get(key).replaceAll('{price}', formattedPrice);
  }
}

class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    return ['en', 'zh', 'ja', 'ko', 'fr', 'ru', 'de', 'es', 'ur', 'id', 'tr', 'vi', 'th', 'hi', 'pt', 'ar', 'pl', 'it', 'nl', 'sv', 'he', 'el'].contains(locale.languageCode);
  }

  @override
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(locale);
  }

  @override
  bool shouldReload(AppLocalizationsDelegate old) => false;
}
